<template>
  <v-card tile :elevation="0" class="crud-box list-users">
    <h1 class="mx-4">Manage Users</h1>
    <v-layout wrap class="px-4 pt-4">
      <div style="overflow: visible">
        <v-btn color="success" class="mr-2" @click="onClickNew">
          <v-icon>mdi-plus</v-icon>
          New
        </v-btn>
        <v-btn color="error" v-if="pickedItems.length > 0" class="mr-2" @click="onClickDeleteAll">
          <v-icon>mdi-delete</v-icon>
          Delete Items
        </v-btn>
      </div>
      <!---->
      <v-spacer></v-spacer>
      <v-layout wrap style="max-width: calc(100% - 250px);">
        <v-text-field dense outlined @keyup.enter="doSearch" class="mr-2" label="Search Name"
                      v-model="searchFields.name.value"/>
        <v-text-field dense outlined @keyup.enter="doSearch" class="mr-2" label="Search Email"
                      v-model="searchFields.email.value"/>
        <v-autocomplete dense outlined @keyup.enter="doSearch" class="mr-2" label="Search Role"
                        :items="listSearchRoles" v-model="searchFields.role_id.value"/>
      </v-layout>
      <v-btn color="primary" @click="doSearch">
        <v-icon>mdi-magnify</v-icon>
        Search
      </v-btn>
    </v-layout>

    <!--card content-->
    <v-card tile :elevation="1" :disabled="tblLoading" outlined class="block-tbl mx-4">
      <v-data-table :headers="tblConfig.header" :items="listItems" :server-items-length="tblPagination.total" :loading="tblLoading"
                    :footer-props="tblConfig.footerProps" @dblclick:row="onClickRow" :options.sync="tblOptions"
                    :loading-text="tblConfig.text.loading" v-model="pickedItems" :show-select="tblConfig.showSelect">
        <template v-slot:no-data>
          {{ tblConfig.text.noItem }}
        </template>

        <template v-slot:item.no="{item}">
          {{ getIndexOfItem(item) }}
        </template>

        <template v-slot:item.actions="props">
          <v-icon small class="mr-2">mdi-view-list</v-icon>
          <v-icon small @click="onClickEdit(props)" class="mr-2">mdi-pencil</v-icon>
          <v-icon small @click="onClickDelete(props)">mdi-delete</v-icon>
        </template>
      </v-data-table>
    </v-card>
    <v-dialog v-model="dialogUpdate" :width="800">
      <UpdateItem :onUpdated="onUpdated" :selectedItem="selectedItem" :onCancel="()=>{dialogUpdate=false}"></UpdateItem>
    </v-dialog>
    <v-dialog v-model="dialogCreate" :width="800">
      <CreateItem :onCreated="onCreated" :onCancel="()=>{dialogCreate=false}"></CreateItem>
    </v-dialog>
    <v-dialog v-model="dialogDelete" :width="400">
      <DeleteItem :idsToDelete="idsToDelete" :onDeleted="onDeleted" :onCancel="()=>{dialogDelete=false}"></DeleteItem>
    </v-dialog>
  </v-card>
</template>
<script>
import {mapActions, mapGetters} from 'vuex'
import DeleteItem from "@/admin/pages/ManageUsers/Delete";
import CreateItem from "@/admin/pages/ManageUsers/Create";
import UpdateItem from "@/admin/pages/ManageUsers/Update";

export default {
  name: 'ManageUsers',
  components: {DeleteItem, CreateItem, UpdateItem},
  data() {
    return {
      pickedItems: [],
      selectedItem: null,
      idsToDelete: [],
      tblLoading: false,
      dialogCreate: false,
      dialogUpdate: false,
      dialogDelete: false,
      searchFields: {
        name: {
          value: '',
          type: 'like',
        },
        email: {
          value: '',
          type: 'like',
        },
        role_id: {
          value: '',
          type: '='
        },
      },
      querySilent: true,
      searchWith: 'role;socialAccount',
      tblConfig: {
        header: [
          {
            text: 'No',
            value: 'no',
            sortable: false,
            groupable: false,
            width: '55px',
            align: 'start',
          },
          {
            text: 'Name',
            value: 'name',
          },
          {
            text: 'Email',
            value: 'email',
          },
          {
            text: 'Role',
            value: 'role_name',
          },
          {
            text: 'Created At',
            value: 'created_at',
          },
          {
            text: 'Updated At',
            value: 'updated_at',
          },
          {
            text: 'Actions',
            value: 'actions',
            sortable: false,
            groupable: false,
            width: '111px',
            align: 'center',
          }
        ],
        footerProps: {
          showFirstLastPage: true,
          'items-per-page-options': [10, 20, 50, 100, -1]
        },
        showSelect: true,
        text: {
          loading: 'Loading ...',
          noItem: 'No item found'
        },
      },
      tblOptions: {}
    }
  },
  computed: {
    ...mapGetters({
      users: 'user/all',
      roles: 'role/all',
      roleById: 'role/getById',
      tblPagination: 'user/pagination'
    }),
    listItems() {
      return this.users.map(u => {
        u.role_name = u.role.name;
        return u;
      })
    },
    listSearchRoles() {
      let listSearchRoles = [
        {text: 'All Roles', value: null}
      ]
      this.roles.forEach(r => {
        listSearchRoles.push({
          text: r.name,
          value: r.id
        })
      });
      return listSearchRoles;
    },
    getIndexOfItem() {
      return item => {
        let pageOffset = this.tblPagination ? (this.tblPagination.current_page - 1) * this.tblPagination.per_page : 0
        return pageOffset + this.users.indexOf(item) + 1
      }
    },
    queryConf() {
      return {
        options: this.tblOptions,
        searchWith: this.searchWith,
        querySilent: this.querySilent
      }
    }
  },
  methods: {
    ...mapActions({
      getListItems: 'user/get',
      getAllRoles: 'role/all',
    }),
    onClickRow($event, row) {
      console.log('$event', $event);
      console.log('row', row);
    },

    onClickNew() {
      this.dialogCreate = true;
    },

    /**
     * open confirm box for delete 1 item
     * @param props
     */
    onClickDelete(props) {
      this.dialogDelete = true;
      this.idsToDelete = [props.item.id];
    },

    onClickEdit(props) {
      this.selectedItem = props.item;
      this.dialogUpdate = true;
    },

    /**
     * open confirm box for delete multi item
     */
    onClickDeleteAll() {
      this.dialogDelete = true;
      this.idsToDelete = this.pickedItems.map(i => i.id);
    },

    /**
     * callback after delete items
     */
    onDeleted() {
      this.dialogDelete = false;
      this.idsToDelete = [];
      this.doSearch();
    },

    onCreated() {
      this.dialogCreate = false;
      this.doSearch();
    },

    onUpdated() {
      this.dialogUpdate = false;
      this.doSearch();
    },

    doSearch() {
      this.pickedItems = [];
      this.selectedItem = null;
      this.tblLoading = true
      this.getListItems({
        ...this.queryConf,
        searchFields: this.searchFields,
      }).then(res => {
        if (this.tblOptions.page > res.data.total_pages) this.tblOptions.page = res.data.total_pages;
      }).finally(() => this.tblLoading = false);
    },
  },
  watch: {
    'queryConf': {
      handler(val) {
        console.log('queryConf:', val);
        this.doSearch();
      },
      deep: true
    },
  },
  created() {
    console.log('roleById:', this.roleById(1));
  }
}
</script>
